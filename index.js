/* ======================
    MENU RESPONSIVE
======================*/

const iconMenu = document.getElementById("iconMenu");
const nav = document.getElementById("nav");
const bgBlackMenu = document.getElementById("bgBlackMenu");

iconMenu.addEventListener("click", () => {
  nav.style.right = "0px";
  nav.style.transition = "all 300ms ease";
  bgBlackMenu.style.display = "block";
});

bgBlackMenu.addEventListener("click", () => {
  nav.style.right = "-250%";
  nav.style.transition = "all 300ms ease";
  bgBlackMenu.style.display = "none";
});

